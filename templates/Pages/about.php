<h1 class="mb-3 text-center">About</h1>
<br><br>
<h5 class="mb-3 text-center">About the Message Boards</h5>
<p>
   The online interaction I enjoyed, sharing jokes, recipes, and disputes on the bulletin boards 
   of the middle 1990s were the inspiration for this website. I even met my wife on one of those 
   boards! It is possible to stumble upon a board that is so engaging that you can spend hours 
   reading it and getting to know the people who make such places special. 
</p>
<br><br>
<h5 class="mb-3 text-center">About the Application</h5>
<p>
   This application was built using the CakePHP framework. It is a simple message board system 
   using a MySQL database on the back end to store messages and boards. The front end is built 
   using the Bootstrap CSS framework. The application is designed to be simple and easy to use.
</p>
<br><br>
<h5 class="mb-3 text-center">About the Performance</h5>
<p>
   This application is running on an affordable, shared hosting plan from Dreamhost, and it is 
   not exactly blazing fast for this reason. 
</p>
<br><br>
<h5 class="mb-3 text-center">About the Codebase Under the Hood</h5>
<p>
   It's written mostly in PHP (currently running v8.3) and it is (or will soon be) entirely 
   available on GitHub. It has an MIT License, meaning that you are free to take it and change 
   it to suit your needs. There is no administrative back-end as one would expect in a forum 
   system. I use the MySQL management application <em>phpMyAdmin</em> to manage the database, 
   and it is a sufficient tool for the job.
</p>
